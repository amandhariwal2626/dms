import type { Prisma } from '@prisma/client';

export function createDefaultCompanySettings(companyId: string): Prisma.CompanySettingsCreateInput {
  return {
    company: { connect: { id: companyId } },
    defaultLanguage: 'EN',
    defaultTimezone: 'ASIA_KOLKATA',
    defaultCurrency: 'INR',
    dateFormat: 'DD/MM/YYYY',
    timeFormat: 'HH:mm',
    weekStartsOn: 1,
    financialYearStartMonth: 4,
    decimalPrecision: 2,
    quantityPrecision: 2,
    pricePrecision: 2,
    weightPrecision: 2,
    autoGenerateProductCode: true,
    autoGenerateCustomerCode: true,
    autoGenerateDistributorCode: true,
    autoGenerateSalesOrderNumber: true,
    autoGenerateInvoiceNumber: true,
    allowNegativeInventory: false,
    enableBatchTracking: false,
    enableExpiryTracking: false,
    enableSerialNumberTracking: false,
    enableMultiWarehouse: false,
    stockReservationEnabled: false,
    gstEnabled: true,
    tdsEnabled: false,
    tcsEnabled: false,
    sessionTimeout: 30,
    maxConcurrentSessions: 5,
    enableTwoFactor: false,
    ipWhitelistingEnabled: false,
    approvalWorkflowEnabled: false,
    salesApprovalRequired: false,
    purchaseApprovalRequired: false,
    paymentApprovalRequired: false,
    returnApprovalRequired: false,
  } satisfies Prisma.CompanySettingsCreateInput;
}
